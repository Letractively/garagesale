<h2><span class="editsection"><a title="Editar seção: Proposta" href="/wikitcc/index.php?title=P%C3%A1gina_principal&amp;action=edit&amp;section=4"></a></span> <span id="Proposta" class="mw-headline">Proposta</span></h2>
<p>Desenvolvimento de um sistema web de mercado aberto que permite o cadastro de produtos usados para venda, troca ou doação. O sistema, interagindo com o do google maps, busca facilitar a busca por produtos em desuso e estimular a sua reutilização com o fim de diminuir o consumismo e a produção desnecessária de lixo.
</p>
<h2><span class="editsection"><a title="Editar seção: Descrição" href="/wikitcc/index.php?title=P%C3%A1gina_principal&amp;action=edit&amp;section=5"></a></span> <span id="Descri.C3.A7.C3.A3o" class="mw-headline">Descrição</span></h2>
<p>Será criado um portal para auxiliar na reutilização de produtos em desuso. O site permitirá que usuários cadastrem seus utensílios obsoletos e que busquem por produtos cadastrados. O site não se responsabiliza pela venda do produto, apenas pela comunicação entre o vendedor e o comprador. O projeto tem como finalidade diminuir o consumismo, a produção de lixo assim como facilitar a aquisição de produtos.
</p>
<h2><span class="editsection"><a title="Editar seção: Problema Identificado" href="/wikitcc/index.php?title=P%C3%A1gina_principal&amp;action=edit&amp;section=6"></a></span> <span id="Problema_Identificado" class="mw-headline">Problema Identificado</span></h2>
<p>O problema inicilamente identificado é a grande produção de lixo de forma desnecessária, enquanto algumas pessoas necessitam de determinados produtos outros estão jogando fora.
</p>
<ul><li>Consumo/Produção de lixo. 
</li><li>Produtos utilizáveis sendo jogado fora. 
</li><li>Dificuldade de encontrar quem necessita dos produtos julgados obsoletos pelo dono.
</li></ul>
<p>O consumismo e a produção de lixo estão se tornando problemas graves e não há justificativa para continuarem existindo. Usualmente as pessoas são inconsequentes ao comprarem eletrodomésticos, móveis, eletroportáteis ou brinquedos que são utilizados por pouco tempo ou substituídos por um mais atual e ao mesmo tempo não há mais espaço para serem depositados, contudo estes produtos, julgados obsoletos por uns, são escassos para outros. 
</p>
